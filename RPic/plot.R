##scalability
nodes=c('04','06','08','10','12','14','16','04','06','08','10','12','14','16','04','06','08','10','12','14','16')
dataset=c('05m','05m','05m','05m','05m','05m','05m','30m','30m','30m','30m','30m','30m','30m','50m','50m','50m','50m','50m','50m','50m')
time=c(94.98,661.755,74.31,62.955,367.04,300.125,160.49,288.755,211.515,760.85,145.675,608.245,188.255,208.47,718.12,854.34,507.305,444.59,921.93,602.815,446.575)
avgData=tapply(time,list(nodes,dataset),mean)
avgData=cbind(c(4,6,8,10,12,14,16),avgData)
avgData=data.frame(avgData)
colnames(avgData)=c("NODES","05m","30m","50m")
avgData=melt(avgData,id=1)
colnames(avgData)=c("NODES","DATA","TIME")
ggplot(avgData,aes(x=NODES,y=TIME,color=DATA))+geom_line(size=1.5)+ylab("Time")
ggsave("scalability.png",w=6,h=4,dpi=400)

